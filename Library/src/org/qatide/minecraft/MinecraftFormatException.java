package org.qatide.minecraft;

/**
 * @author Omicron
 */
public final class MinecraftFormatException extends Exception {
	public MinecraftFormatException() {
	}

	public MinecraftFormatException(String message) {
		super(message);
	}

	public MinecraftFormatException(String message, Throwable cause) {
		super(message, cause);
	}

	public MinecraftFormatException(Throwable cause) {
		super(cause);
	}
}
